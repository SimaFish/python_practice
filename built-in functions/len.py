# The len() function returns the number of items in an object.
# When the object is a string, the len() function returns the
# number of characters in the string.

# An object must be a sequence or a collection.
# Returns the value of integer data type.

# str - sequence
test_string = 'Hello, world!'
print('\nLength of string:', len(test_string))

# tuple - collection
test_tuple = (42, 62, 'Test', 3.45)
print('Length of tuple:', len(test_tuple))

# list - collection
test_list = ['apple', 'banana', 'cherry']
print('Length of list:', len(test_list), '\n')